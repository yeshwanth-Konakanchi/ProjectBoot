package com.crmindz.bcjmay2017.oops.classes.simple.lab3;

/**
 * @author Yeshwanth Konakanchi
 *This class will have properties of Employee and setters and getters
 */
public class Employee {
	
	private int employeeId;
	private char gender;
	private float allowances;
	private double basicSalary;
	
	public Employee(int employeeId){
		this.employeeId = employeeId;
		
	}
	
	public void printEmployeeDetails(Employee employee){
		
		System.out.println(employee.gender);
		System.out.println(employee.employeeId);
		System.out.println(employee.allowances);
		System.out.println(employee.basicSalary);
		
	}
	
	public static void main(String[] args) {
		
		Employee employee = new Employee(14305);
		employee.gender = 'M';
		employee.allowances =   11110.54f;
		employee.basicSalary = 65000.00;
		
		employee.printEmployeeDetails(employee);
		
		
	}
}
