package com.nivtek.training.plusplus;

public class DemoPrefixAndSuffixPlusPlus {

  /**
   * @param args
   */
  public static void main(String[] args) {
    int k = 5;
    // increment before assignment => k = k + 1; i = k; 
    int i = ++k; 
    System.out.println("Value of k = " + k);
    System.out.println("Value of i = " + i);

    System.out.println();
    System.out.println("---------------------");
    System.out.println();
    
    int kk = 5;
    // increment after assignment => ii = kk; kk = kk + 1;
    int ii = kk++; 
    System.out.println("Value of kk = " + kk);
    System.out.println("Value of ii = " + ii);

    System.out.println();
    System.out.println("---------------------");
    System.out.println();
    
    // Value of the expression n++ is n and not n+1
    int n = 0;
    while ( n < 10 ) {
       System.out.println(n++);
       System.out.println(n);
    }
    
    System.out.println();
    System.out.println("---------------------");
    System.out.println();
    
    // Value of the expression ++m is m+1 and not m
    int m = 0;
    while ( m < 10 ) {
       System.out.println(++m);
       System.out.println(m);
    }
  }
}
